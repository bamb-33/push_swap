#include "../header.h"

int	is_sorted(t_data *x)
{
	int	i;

	i = 0;
	while (i < stack_len(x->hm_a) - 1)
	{
		if (x->a[i][0] < x->a[i + 1][0])
			return (0);
		i++;
	}
	return (1);
}
